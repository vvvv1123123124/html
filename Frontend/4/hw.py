a = int(input('Введите первое число:'))
b = int(input('Введите второе число:'))
c = int(input('Введите третье число:'))

addition = a + b + c
product = a * b * c
average = addition/3

print(a,'+',b,'+',c,'=',addition)
print(a,'*',b,'*',c,'=',product)
print('Среднее арифметическое чисел ',a,',',b,'и',c,'это ',average)
